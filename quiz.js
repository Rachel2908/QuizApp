let currentQuestion = 0;
let score = 0;
let userAnswers = [];


function showQuiz() {
  document.getElementById('quiz-container').classList.remove('hidden');
  document.getElementById('score-container').classList.add('hidden');
  renderQuestion();
}

function renderQuestion() {
  const quizContainer = document.getElementById('quiz-container');
  const currentQuestionData = questions[currentQuestion];

  if (currentQuestion < questions.length) {
    // Display question and options
    let optionsHtml = '';
    currentQuestionData.options.forEach((option, index) => {
      optionsHtml += `<label style="color:black">
                        <input style="margin-top:50px" type="radio" name="answer" value="${index}" required>
                        ${option}
                      </label><br>`;
    });

    quizContainer.innerHTML = `<h3 style="color: black;  margin-top: 3rem; font-size:0.85rem">${currentQuestionData.question}</h3>${optionsHtml}
                               <button style="padding:10px 15px; border-radius:10px; font-size:1rem; margin-top:15px" onclick="submitAnswer()" >Submit</button>`;
  } else {
    // Quiz completed, show score
    showScore();
  }
}

function submitAnswer() {
  const selectedOption = document.querySelector('input[name="answer"]:checked');

  if (selectedOption) {
    const userAnswer = parseInt(selectedOption.value);
    userAnswers.push(userAnswer);

    const currentQuestionData = questions[currentQuestion];

    if (userAnswer === currentQuestionData.correctAnswer) {
      score++;
    }

    currentQuestion++;
    renderQuestion();
  }
}

function showScore() {
  document.getElementById('quiz-container').classList.add('hidden');
  const scoreContainer = document.getElementById('score-container');
  scoreContainer.classList.remove('hidden');

  const totalQuestions = questions.length;
  scoreContainer.innerHTML = `<h2 style="text-align:center; margin-bottom:1rem; margin-top:2rem">Quiz Completed</h2>
                              <p style="text-align:center; font-size:0.8rem;
                              font-weight:500; margin-bottom:1rem">Your score: ${score}/${totalQuestions}</p>
                              <button style="margin-left:10px; padding:10px 15px; border-radius:10px; font-size:1rem; margin-top:10px" onclick="showIncorrectAnswers()">View Incorrect Answers</button>
                              <a href="#" onclick="restartQuiz()" style="text-decoration:none; color:rgb(85, 1, 153); font-weight:500" >Restart Quiz</a>`;
}

function showIncorrectAnswers() {
  const scoreContainer = document.getElementById('score-container');
  scoreContainer.innerHTML += `<p style="text-align:center; margin-bottom:1rem; margin-top:1rem;color: rgb(204, 3, 37); font-size:1.2rem">Incorrect Answers:</p>`;

  questions.forEach((question, index) => {
    if (question.correctAnswer !== userAnswers[index]) {
      scoreContainer.innerHTML += `<p style="text-align:center; margin-bottom:1rem; margin-top:1rem; font-size:1.2rem">${question.question}<br>
                                   Your Answer: ${question.options[userAnswers[index]]}<br>
                                   Correct Answer: ${question.options[question.correctAnswer]}</p>`;
    }
  });
}

function restartQuiz() {
  currentQuestion = 0;
  score = 0;
  userAnswers = [];
  const scoreContainer = document.getElementById('score-container');
  scoreContainer.innerHTML = '';
  showQuiz();
}

document.addEventListener('DOMContentLoaded', showQuiz);

